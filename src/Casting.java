
public class Casting {

	public static void main(String[] args) {

	int a=10;
	float b=a;
	System.out.println(a);
	System.out.println(b);
	
	float f=20f;
	int c=(int)f;
	System.out.println(f);
	System.out.println(c);
	
	 int x=5, y=7;
	   x=x+y;
	   y=x-y;
	   x=x-y;
	   System.out.println("x:"+" "+x);
	   System.out.println("y:"+" "+y);
	} 
		   
}
